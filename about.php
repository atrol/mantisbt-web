<span class="section">Versioning</span>
<p>
The release numbering convention used is <b>major.minor.micro</b> (eg. 1.1.0).
<ul>
	<li>Major - Indicates a very large change in the core package.  Rewrites or major milestones.</li>
	<li>Minor - Significant amount of feature addition/modification.</li>
	<li>Micro - Usually bug fixes or minor features.</li>
</ul>
<p>Sometimes there is an a1, a2, rc1, rc2 - these are for alpha releases and release candidates.</p>
