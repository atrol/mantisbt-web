<h4>Links</h4>

<h5>MantisConnect</h5>
<p>
	<a href="http://www.futureware.biz/mantisconnect/">MantisConnect</a> 
	allows the following applications to integrate with MantisBT:
</p>
<ul>
	<li>.NET Applications</li>
	<li>Java Applications</li>
	<li>Cocoa Applications</li>
	<li>Eclipse IDE</li>
	<li>NAnt Build Scripts</li>
	<li>Applications that can access webservices</li>
</ul>

<h5>MantisWAP</h5>
<p>
	<a href="http://www.futureware.biz/mantiswap">MantisWAP</a> provides WAP 
	enabled phones and PDAs with easy access to MantisBT Bug Tracker. This 
	is a great asset for support staff and developers who need to access 
	MantisBT while on the go. MantisWAP can easily be integrated with 
	existing MantisBT installations without having to modify the existing 
	MantisBT code.
</p>

